def Key2Mac(key:str):
    '''
    根据关键词返回对应的mac地址
    '''
    outcome = []
    for gateway in device_lib:
        temp = []
        for device in device_lib[gateway]['Devices']:
            if key in device_lib[gateway]['Devices'][device]['Name']:
                temp.append(device)
        outcome.append({gateway:temp})

    return outcome



